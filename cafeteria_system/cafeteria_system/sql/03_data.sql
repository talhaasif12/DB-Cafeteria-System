/* -----------------------------------------------------------------------------
   Update totals (keeps ORDER_ITEM subtotal check intact)
   Apply discounts: Student 10%, Staff 5%, Guest 0%.
   --------------------------------------------------------------------------- */
UPDATE "ORDER" o
SET TOTAL_AMOUNT =
  (SELECT ROUND(SUM(oi.SUBTOTAL),2) FROM ORDER_ITEM oi WHERE oi.ORDER_ID = o.ORDER_ID);

-- Apply discount to completed + pending (project rule: discount at ordering time)
UPDATE "ORDER" o
SET TOTAL_AMOUNT = ROUND(
  TOTAL_AMOUNT * (1 - (
    SELECT d.DISCOUNT_PERCENT/100
    FROM DISCOUNT d
    JOIN CUSTOMER c ON c.CUSTOMER_ID = o.CUSTOMER_ID
    WHERE d.CUSTOMER_TYPE = c.CUSTOMER_TYPE
      AND TRUNC(o.ORDER_DATE) BETWEEN d.VALID_FROM AND d.VALID_TO
  )), 2
);

